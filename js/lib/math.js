const EPSILON = Number.EPSILON

export const clamp = (value, min, max) => (
    Math.min(Math.max(value, min), max)
)

export const lerp = (min, max, t) => (
    min * (1 - t) + max * t
)

export const inverseLerp = (min, max, t) => (
    Math.abs(min - max) < EPSILON ? 0 : (t - min) / (max - min)
)

export const smoothstep = (min, max, t) => {
    const x = clamp(inverseLerp(min, max, t), 0, 1)
    return x * x * (3 - 2 * x);
}

export const mod = (a, b) => (
    ((a % b) + b) % b
)

export const degToRad = (n) => (
    n * Math.PI / 180
)

export const radToDeg = (n) => (
    n * 180 / Math.PI
)
  
export const fract = (n) => (
    n - Math.floor(n)
)

export const sign = (n) => (
    Math.sign(n)
)

